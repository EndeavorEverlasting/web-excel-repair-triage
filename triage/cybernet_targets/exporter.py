from dataclasses import asdict
from pathlib import Path
from typing import Any, Dict, List, Optional

from triage.cybernet_targets.config import targets_schema
from triage.cybernet_targets.models import TargetRow, rows_to_dicts
from triage.cybernet_targets.resolver import AmbReconciliationRow, ResolverReport
from triage.cybernet_targets.compare import CompareReport
from triage.xlsx_utils import fix_inlinestr


def _require_openpyxl():
    try:
        from openpyxl import Workbook
        from openpyxl.styles import Font, PatternFill, Alignment
        from openpyxl.utils import get_column_letter
    except ImportError as e:
        raise RuntimeError("openpyxl is required: pip install openpyxl") from e
    return Workbook, Font, PatternFill, Alignment, get_column_letter


def _write_data_sheet(ws, headers: List[str], rows: List[Dict[str, Any]], header_row: int = 1) -> None:
    _, Font, PatternFill, Alignment, get_column_letter = _require_openpyxl()
    fill = PatternFill("solid", fgColor="1F365C")
    for c, h in enumerate(headers, 1):
        cell = ws.cell(row=header_row, column=c, value=h)
        cell.font = Font(bold=True, color="FFFFFF")
        cell.fill = fill
        cell.alignment = Alignment(horizontal="center")
    for r_idx, row in enumerate(rows, header_row + 1):
        for c, h in enumerate(headers, 1):
            ws.cell(row=r_idx, column=c, value=row.get(h, ""))
    last_row = max(header_row, len(rows) + header_row)
    last_col = get_column_letter(len(headers))
    ws.freeze_panes = ws.cell(row=header_row + 1, column=1).coordinate
    ws.auto_filter.ref = f"A{header_row}:{last_col}{last_row}"


def _targets_for_site(targets: List[TargetRow], site: str) -> List[Dict[str, Any]]:
    cols = targets_schema()["target_columns"]
    return [{k: r.to_dict().get(k, "") for k in cols} for r in targets if r.site == site]


def build_shortage_rows(targets: List[TargetRow]) -> List[Dict[str, Any]]:
    out: List[Dict[str, Any]] = []
    for tr in targets:
        if tr.cybernet_count <= 0 and tr.kit_required != "yes":
            continue
        out.append({
            "Target ID": tr.target_id,
            "Site": tr.site,
            "Location": tr.location,
            "Cybernet Required": tr.cybernet_count or 1,
            "On Hand": "TBD",
            "Shortage": "TBD",
            "Action Needed": tr.action_needed or "Link to inventory master",
        })
    return out


def build_kit_requirements(targets: List[TargetRow]) -> List[Dict[str, Any]]:
    headers = ["Site", "Location", "Cybernet Count", "Neuron Count", "Arm Required", "Ethernet DIM Required", "Breakaway Required"]
    return [
        {
            "Site": tr.site,
            "Location": tr.location,
            "Cybernet Count": tr.cybernet_count or (1 if tr.target_type.lower() == "cybernet" else 0),
            "Neuron Count": tr.neuron_count or 1,
            "Arm Required": tr.arm_required or "TBD",
            "Ethernet DIM Required": tr.ethernet_dim_required or "TBD",
            "Breakaway Required": tr.breakaway_required or "TBD",
        }
        for tr in targets
        if tr.target_type.lower() == "cybernet" or tr.cybernet_count > 0
    ]


def export_websafe_target_dashboard(
    targets: List[TargetRow],
    resolver_report: ResolverReport,
    compare_report: CompareReport,
    out_path: str,
    as_of: str,
    manifest_inputs: Dict[str, Any],
    existing_dashboard_path: Optional[str] = None,
) -> str:
    Workbook, Font, PatternFill, Alignment, get_column_letter = _require_openpyxl()
    schema = targets_schema()
    cols = schema["target_columns"]
    wb = Workbook()
    wb.remove(wb.active)

    # Start Here
    ws_start = wb.create_sheet("Start Here")
    ws_start["A1"] = "Cybernet Target Sprint Dashboard"
    ws_start["A2"] = f"As of {as_of} — generated by triage.cybernet_targets"
    ws_start["A3"] = "See docs/CYBERNET_TARGET_SPRINT_CONTRACT.md"

    # Dashboard summary
    ws_dash = wb.create_sheet("Dashboard")
    counts = {}
    for site in schema["sprint_site_tabs"]:
        counts[site] = sum(1 for t in targets if t.site == site)
    ws_dash.append(["Site", "Count", "Blank Hostnames", "Blocked Readiness"])
    for site in schema["sprint_site_tabs"]:
        site_t = [t for t in targets if t.site == site]
        blank = sum(1 for t in site_t if not (t.hostname or "").strip())
        blocked = sum(1 for t in site_t if t.readiness_gate == "blocked")
        ws_dash.append([site, len(site_t), blank, blocked])
    ws_dash.append([])
    ws_dash.append(["Total Active Targets", sum(counts.values())])
    ws_dash.freeze_panes = "A2"

    for site in ("HH", "JTM", "SSUH"):
        ws = wb.create_sheet(site)
        _write_data_sheet(ws, cols, _targets_for_site(targets, site))

    ws_amb_g = wb.create_sheet("AMB_Grouped")
    _write_data_sheet(ws_amb_g, cols, _targets_for_site(targets, "AMB"))

    amb_raw_cols = cols
    ws_amb_r = wb.create_sheet("AMB_Raw_Source")
    _write_data_sheet(ws_amb_r, amb_raw_cols, rows_to_dicts(resolver_report.amb_raw))

    recon_cols = ["layer", "source_row", "practice_location", "group_key", "in_sprint", "in_wave3_cybernet", "in_wave2_ane", "action_needed"]
    ws_recon = wb.create_sheet("AMB_Reconciliation")
    _write_data_sheet(ws_recon, recon_cols, [r.to_dict() for r in resolver_report.amb_reconciliation])

    kit_cols = ["Site", "Location", "Cybernet Count", "Neuron Count", "Arm Required", "Ethernet DIM Required", "Breakaway Required"]
    ws_kit = wb.create_sheet("Kit_Requirements")
    _write_data_sheet(ws_kit, kit_cols, build_kit_requirements(targets))

    shortage_cols = ["Target ID", "Site", "Location", "Cybernet Required", "On Hand", "Shortage", "Action Needed"]
    ws_short = wb.create_sheet("Shortages")
    _write_data_sheet(ws_short, shortage_cols, build_shortage_rows(targets))

    carry_cols = ["target_id", "site", "location", "action", "columns_carried", "note"]
    ws_carry = wb.create_sheet("Carryover_Log")
    _write_data_sheet(ws_carry, carry_cols, [e.to_dict() for e in compare_report.carryover_log])

    ws_def = wb.create_sheet("Definitions_Current")
    ws_def.append(["Field", "Definition"])
    for c in cols:
        ws_def.append([c, "See CYBERNET_TARGET_SPRINT_CONTRACT.md"])

    ws_drop = wb.create_sheet("Dropdown_Values")
    ws_drop.append(["Column", "Allowed Values"])
    ws_drop.append(["Readiness Gate", "blocked, in_progress, staged, ready"])
    ws_drop.append(["Asset Assignment Status", "unassigned, assigned, partial"])

    ws_cf = wb.create_sheet("00_CF_Dictionary")
    ws_cf.append(["Rule ID", "Description"])
    ws_cf.append(["READINESS_BLOCKED", "Hostname missing or checklist incomplete"])
    if existing_dashboard_path:
        try:
            from openpyxl import load_workbook
            src = load_workbook(existing_dashboard_path, read_only=True, data_only=True)
            cf_name = "00_CF_Dictionary" if "00_CF_Dictionary" in src.sheetnames else "CF_Dictionary"
            if cf_name in src.sheetnames:
                for row in src[cf_name].iter_rows(values_only=True):
                    if row and any(row):
                        ws_cf.append(list(row))
            src.close()
        except Exception:
            ws_cf.append(["SPRINT_V1", "Default sprint CF rules"])

    ws_notes = wb.create_sheet("Repo_Automation_Notes")
    ws_notes.append(["Key", "Value"])
    for k, v in manifest_inputs.items():
        ws_notes.append([k, str(v)])

    Path(out_path).parent.mkdir(parents=True, exist_ok=True)
    wb.save(out_path)
    fix_inlinestr(out_path)
    return out_path


def write_sidecar_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, default=str)


def write_reconciliation_csv(path: Path, rows: List[AmbReconciliationRow]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    cols = ["layer", "source_row", "practice_location", "group_key", "in_sprint", "in_wave3_cybernet", "in_wave2_ane", "action_needed"]
    with path.open("w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=cols)
        w.writeheader()
        for r in rows:
            w.writerow(r.to_dict())


def write_shortage_csv(path: Path, targets: List[TargetRow]) -> None:
    cols = ["Target ID", "Site", "Location", "Cybernet Required", "On Hand", "Shortage", "Action Needed"]
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=cols)
        w.writeheader()
        for row in build_shortage_rows(targets):
            w.writerow(row)
